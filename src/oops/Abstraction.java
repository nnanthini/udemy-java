package oops;

public class Abstraction {
	/*
	 * only declare functionality, it is not defined
	 * can be done to both classes and methods
	 * 
	 * abstract class cant be instantiated
	 * abstract method must be overriden in first level of concrete class (immediate child)
	 * abstract -> concrete
	 * abstract -> abstract -> abstract -> concrete
	 * 
	 * abstract methods must be in abstract class, cant have body, must be implemented in first concrete class that extends the abstract class
	 *  
	 * 
	 * 
	 * in abstract interface, all methods must be abstract
	 * in abstract class, methods might be abstract, concrete or both
	 * (we can call concrete methods of abstract class using child class of the abstract method)
	 * 
	 * 
	 * interface is pure abstract class where all methods are abstract
	 * - will always be abstract whether we mention it or not
	 * 
	 * interface & implements 
	 * 
	 * 
	 * abstract class and interfance cant be final
	 * cos final classes and interfaces methods cant be overriden
	 * 
	 * interface extends anotherInteface (since both are by default abstract)
	 * multiple inheritance is possible for interface cos all methods are abstract
	 * 
	 * 
	 */
}
